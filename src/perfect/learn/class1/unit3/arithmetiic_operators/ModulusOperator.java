package perfect.learn.class1.unit3.arithmetiic_operators;

public class ModulusOperator {
    public static void main(String[] args) {
        int x = 42;
        double y = 42.25;
        System.out.println("x mod 10 = " + x % 10);
        System.out.println("y mod 10 = " + y % 10);
    }
}

